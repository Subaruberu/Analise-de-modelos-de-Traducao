"""## 7. Métricas de Superfície — chrF, chrF++ e TER

A Tabela 11 completa (conforme apresentada no slide 13 do TCC) inclui também
três métricas de sobreposição de superfície que revelam **por que o BLEU isolado
é insuficiente** no par IT→PT:

| Métrica | Interpretação | Valor esperado (T5) |
|---|---|---|
| **chrF** | F-score de n-gramas de *caracteres* | Inflado por cognatos IT-PT |
| **chrF++** | chrF + n-gramas de palavras (β=2) | Idem, efeito de falso positivo |
| **TER** | Taxa de edições p/ igualar a referência | Alto = má tradução |

> **Atenção:** chrF e chrF++ elevados **não** indicam boa tradução aqui —
> italiano e português compartilham grafias de raiz latina, inflando o F-score
> mesmo quando nada foi traduzido.

"""

# Métricas de superfície: chrF, chrF++ e TER
# Requer sacrebleu >= 2.0 (já instalado na Célula 1)

# ── chrF (n-gramas de caracteres, ordem 6, β=2) ──────────────────────────
chrf_score = sacrebleu.corpus_chrf(
    SAIDA_T5,
    [REFERENCIA_PT],
    char_order=6,
    word_order=0,      # chrF puro (sem n-gramas de palavras)
    beta=2
).score

# ── chrF++ (chrF + n-gramas de palavras, word_order=2) ───────────────────
chrfpp_score = sacrebleu.corpus_chrf(
    SAIDA_T5,
    [REFERENCIA_PT],
    char_order=6,
    word_order=2,      # chrF++ inclui bigramas de palavras
    beta=2
).score

# ── TER (Translation Edit Rate) — quanto menor, melhor ───────────────────
ter_score = sacrebleu.corpus_ter(
    SAIDA_T5,
    [REFERENCIA_PT]
).score

print(f"chrF   = {chrf_score:.2f}  ← inflado por cognatos IT-PT (falso positivo)")
print(f"chrF++ = {chrfpp_score:.2f}  ← idem, reforça o efeito de proximidade linguística")
print(f"TER    = {ter_score:.2f}  ← quase 100% de edições necessárias (má tradução)")

"""### Interpretação dos valores

- **chrF = 36,79** — aparentemente razoável, mas decorre da coincidência de caracteres
  entre línguas irmãs, não de tradução efetiva.
- **chrF++ = 32,79** — resultado similar ao chrF; a inclusão de bigramas de palavras
  não elimina o "falso positivo" por proximidade linguística.
- **TER = 82,18** — quase 82% dos tokens precisariam ser editados para que a saída
  do T5 se iguale à referência humana. Confirma que nada foi traduzido.

Combinados com **BLEU = 6,24**, **IFC = 0,00** e **RTT = 100,00**, esses valores
compõem a **Tabela 11 completa** do TCC e demonstram que métricas de superfície
sozinhas são insuficientes para detectar a falha de não-tradução no par IT→PT.

"""

