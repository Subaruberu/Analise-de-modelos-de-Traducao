"""## 2. Definição das Métricas"""

def bleu(hipotese, referencia):
    """BLEU de corpus (0–100) entre duas listas de versos alinhados."""
    return sacrebleu.corpus_bleu(hipotese, [referencia]).score


# Termos culturalmente/poeticamente marcados e suas formas esperadas em PT.
TERMOS_MARCADOS = {
    "ermo colle":           ["ermo outeiro", "colina solitaria", "monte ermo"],
    "siepe":                ["sebe"],
    "sovrumani silenzi":    ["silencios sobre-humanos", "sobre-humanos"],
    "profondissima quiete": ["profundissima quietude"],
    "il cor":               ["o coracao"],
    "naufragar":            ["naufragar"],   # cognato: só conta se em frase PT
}


def ifc(saida_versos, mapa_termos):
    """
    Índice de Fidelidade Cultural (0–100).
    Conta a fração de termos marcados cuja forma-alvo em PORTUGUÊS
    aparece na saída. Se a saída está em italiano (não traduzida),
    nenhuma forma-alvo ocorre → IFC = 0.
    """
    texto = " ".join(saida_versos).lower()
    em_portugues = any(marca in texto for marca in
                       ["coracao", "silencios", "quietude", "sebe", "outeiro"])
    if not em_portugues:
        return 0.0
    preservados = sum(
        1 for formas_alvo in mapa_termos.values()
        if any(f.lower() in texto for f in formas_alvo)
    )
    return 100.0 * preservados / len(mapa_termos)


def rtt_bleu(saida_forward, fonte):
    """
    RTT (Round-Trip Translation) — BLEU de ida e volta.
      ida  : IT --T5--> saida_forward
      volta: saida_forward --T5--> reconstrução
    Como a ida foi a identidade, a volta também o é.
    Logo: reconstrução == fonte e BLEU satura em 100 — artefato.
    """
    reconstrucao = list(saida_forward)   # volta = identidade também
    return bleu(reconstrucao, fonte)


print("Funções definidas: bleu(), ifc(), rtt_bleu()")

"""## 3. Cálculo dos Scores"""

bleu_direto = bleu(SAIDA_T5, REFERENCIA_PT)
indice_ifc  = ifc(SAIDA_T5, TERMOS_MARCADOS)
escore_rtt  = rtt_bleu(SAIDA_T5, FONTE_IT)

print(f"BLEU direto (vs. ref. humana) : {bleu_direto:6.2f}")
print(f"IFC (Fidelidade Cultural)     : {indice_ifc:6.2f}")
