# Detecta dispositivo
device = "cuda" if torch.cuda.is_available() else "cpu"
print(f"Usando dispositivo: {device}")

# Modelos
MODELOS = {
    "NLLB":       "facebook/nllb-200-distilled-600M",
    "M2M100-418M":"facebook/m2m100_418M",
    "FLAN-T5":    "google/flan-t5-large",
    "MBART":      "facebook/mbart-large-50-many-to-many-mmt"
}

# Cache para evitar recarregar os modelos a cada chamada
_model_cache = {}

def load_tokenizer_model(model_key):
    if model_key in _model_cache:
        return _model_cache[model_key]
    model_name = MODELOS[model_key]
    if model_key == "MBART":
        tokenizer = MBart50TokenizerFast.from_pretrained(model_name)
        model = MBartForConditionalGeneration.from_pretrained(model_name)
    else:
        tokenizer = AutoTokenizer.from_pretrained(model_name, use_fast=True)
        model = AutoModelForSeq2SeqLM.from_pretrained(model_name)
    model.to(device)
    _model_cache[model_key] = (tokenizer, model)
    return tokenizer, model

# Função de split em sentenças simples
def split_into_sentences(text: str) -> List[str]:
    text = text.strip()
    sents = re.split(r'(?<=[\.\.\?\!])\s+(?=[^\s])', text)
    sents = [s.strip() for s in sents if s.strip()]
    return sents if sents else [text]

# Chunking por tokens estimados via tokenizer
def chunk_sentences_by_token_limit(sentences: List[str], tokenizer, max_tokens: int = 400) -> List[str]:
    chunks = []
    cur = []
    cur_toks = 0
    for s in sentences:
        toks = len(tokenizer.tokenize(s))
        if toks > max_tokens:
            if cur:
                chunks.append(" ".join(cur)); cur = []; cur_toks = 0
            words = s.split()
            piece = []; piece_toks = 0
            for w in words:
                wtoks = len(tokenizer.tokenize(w))
                if piece_toks + wtoks > max_tokens:
                    if piece: chunks.append(" ".join(piece))
                    piece = [w]; piece_toks = wtoks
                else:
                    piece.append(w); piece_toks += wtoks
            if piece: chunks.append(" ".join(piece))
            continue
        if cur_toks + toks <= max_tokens:
            cur.append(s); cur_toks += toks
        else:
            if cur: chunks.append(" ".join(cur))
            cur = [s]; cur_toks = toks
    if cur: chunks.append(" ".join(cur))
    return chunks

# ── NLLB: generate() direto ──────────────────────────────────────────────────
def translate_nllb(text: str, src_lang: str, tgt_lang: str, max_tokens: int = 350) -> str:
    tokenizer, model = load_tokenizer_model("NLLB")
    tokenizer.src_lang = src_lang
    sents  = split_into_sentences(text)
    chunks = chunk_sentences_by_token_limit(sents, tokenizer, max_tokens)
    results = []
    for chunk in chunks:
        inputs = tokenizer(chunk, return_tensors="pt", truncation=True, padding=True).to(device)
        tgt_id = tokenizer.convert_tokens_to_ids(tgt_lang)
        out    = model.generate(**inputs, forced_bos_token_id=tgt_id,
                                max_length=1024, num_beams=4, early_stopping=True)
        results.append(tokenizer.decode(out[0], skip_special_tokens=True))
    return " ".join(results)

# ── M2M100: generate() direto ────────────────────────────────────────────────
def translate_m2m(text: str, src_lang: str, tgt_lang: str, max_tokens: int = 350) -> str:
    tokenizer, model = load_tokenizer_model("M2M100-418M")
    tokenizer.src_lang = src_lang
    sents  = split_into_sentences(text)
    chunks = chunk_sentences_by_token_limit(sents, tokenizer, max_tokens)
    results = []
    for chunk in chunks:
        inputs = tokenizer(chunk, return_tensors="pt", truncation=True, padding=True).to(device)
        tgt_id = tokenizer.get_lang_id(tgt_lang)
        out    = model.generate(**inputs, forced_bos_token_id=tgt_id,
                                max_length=1024, num_beams=4, early_stopping=True)
        results.append(tokenizer.decode(out[0], skip_special_tokens=True))
    return " ".join(results)

# ── FLAN-T5: generate() com prompt ───────────────────────────────────────────
def translate_flan(text: str, max_tokens: int = 250) -> str:
    tokenizer, model = load_tokenizer_model("FLAN-T5")
    sents  = split_into_sentences(text)
    chunks = chunk_sentences_by_token_limit(sents, tokenizer, max_tokens)
    results = []
    for chunk in chunks:
        prompt = f"Translate the following text from Portuguese to Italian:\n\n{chunk}\n\nTranslate:"
        inputs = tokenizer(prompt, return_tensors="pt", truncation=True, padding=True).to(device)
        out    = model.generate(**inputs, max_length=1024, num_beams=4, early_stopping=True)
        results.append(tokenizer.decode(out[0], skip_special_tokens=True))
    return " ".join(results)

# ── MBART: generate() direto ─────────────────────────────────────────────────
def translate_mbart(text: str, src_lang: str, tgt_lang: str, max_tokens: int = 350) -> str:
    tokenizer, model = load_tokenizer_model("MBART")
    tokenizer.src_lang = src_lang
    sents  = split_into_sentences(text)
    chunks = chunk_sentences_by_token_limit(sents, tokenizer, max_tokens)
    tgt_id = tokenizer.lang_code_to_id[tgt_lang]
    results = []
    for chunk in chunks:
        inputs = tokenizer(chunk, return_tensors="pt", truncation=True, padding=True).to(device)
        out    = model.generate(**inputs, forced_bos_token_id=tgt_id,
                                max_length=1024, num_beams=4, early_stopping=True)
        results.append(tokenizer.decode(out[0], skip_special_tokens=True))
    return " ".join(results)

# ── Função central ────────────────────────────────────────────────────────────
def translate_text_all_models(text: str, src="pt", tgt="it"):
    nllb_codes  = {"pt": "por_Latn", "it": "ita_Latn"}
    m2m_codes   = {"pt": "pt",       "it": "it"}   # M2M100 usa ISO 639-1
    mbart_codes = {"pt": "pt_XX",    "it": "it_IT"}

    return {
        "NLLB":       translate_nllb( text, nllb_codes[src],  nllb_codes[tgt]),
        "M2M100_418M":translate_m2m(  text, m2m_codes[src],   m2m_codes[tgt]),
        "FLAN_T5":    translate_flan(  text),
        "MBART":      translate_mbart(text, mbart_codes[src], mbart_codes[tgt]),
    }

# ── Métricas simples ──────────────────────────────────────────────────────────
def jaccard_similarity(a: str, b: str) -> float:
    sa = set(a.lower().split()); sb = set(b.lower().split())
    if not sa and not sb: return 1.0
    if not sa or  not sb: return 0.0
    return len(sa & sb) / len(sa | sb)

def overlap_ratio(a: str, b: str) -> float:
    ta = a.lower().split(); tb = set(b.lower().split())
    if not ta: return 0.0
    return sum(1 for t in ta if t in tb) / len(ta)

# ── Processador de pares REF x AUTO ──────────────────────────────────────────
def process_pairs(ref_texts, auto_texts, src="pt", tgt="it"):
    rows = []
    n = min(len(ref_texts), len(auto_texts))
    for i in range(n):
        ref = ref_texts[i]; auto = auto_texts[i]
        tr_ref  = translate_text_all_models(ref,  src=src, tgt=tgt)
        tr_auto = translate_text_all_models(auto, src=src, tgt=tgt)
        for model_name in tr_ref:
            rows.append({
                "Paragrafo_Index": i + 1,
                "Modelo":          model_name,
                "REF_pt":          ref,
                "AUTO_pt":         auto,
                "REF_tr":          tr_ref[model_name],
                "AUTO_tr":         tr_auto[model_name],
                "Jaccard":         jaccard_similarity(tr_ref[model_name], tr_auto[model_name]),
                "Overlap_ratio":   overlap_ratio(tr_ref[model_name], tr_auto[model_name]),
            })
    return pd.DataFrame(rows)

