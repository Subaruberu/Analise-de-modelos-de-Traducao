def calcular_metricas(traducao, referencia):
    """Calcula METEOR, chrF e TER para um par (tradução, referência)."""
    try:
        meteor = single_meteor_score(
            nltk.word_tokenize(str(referencia), language='portuguese'),
            nltk.word_tokenize(str(traducao), language='portuguese')
        ) * 100
    except Exception:
        meteor = 0.0
    chrf = sacrebleu.sentence_chrf(str(traducao), [str(referencia)]).score
    ter  = sacrebleu.sentence_ter(str(traducao), [str(referencia)]).score
    return {'METEOR': round(meteor, 2), 'chrF': round(chrf, 2), 'TER': round(ter, 2)}

